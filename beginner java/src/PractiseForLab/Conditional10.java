
package PractiseForLab;

public class Conditional10 {
    public static void main(String[] args) {
        
        int i;
        System.out.print("The first 10 natural numbers are :\n");
        for(i=1;i<=10;i++)
        {
            System.out.println(i);
        }
        System.out.println("\n");
    }
    
}
